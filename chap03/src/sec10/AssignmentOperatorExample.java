package sec10;

public class AssignmentOperatorExample {

	public static void main(String[] args) {
		
		int r = 0;
		r += 10;
		System.out.println(r);
		
		r -=5;
		System.out.println(r);
		
		r *=3;
		System.out.println(r);
		
		r/=3;
		System.out.println(r);
		
		r%=3;
		System.out.println(r);
		

	}

}
